    print("",end="  ")
    for i in range(0,len(M)) :
        if i < 10 :
            print(" "+chr(i%10+48)+" ",end="")
        else :
            print(chr(i//10+48)+chr(i%10+48)+" ",end="")
    for i in range(0,len(M)) :
        print("                       ")
        if i < 10 :
            print(" "+chr(i%10+48),end="")
        else :
            print(chr(i//10+48)+chr(i%10+48),end="")
        for j in range(0,len(M)) :
            if M[j][i] == 0 :
                print("\033[92m░░░\033[0m",end="")
            elif M[j][i] == 1 :
                print("\033[93m▓▓▓\033[0m",end="")
